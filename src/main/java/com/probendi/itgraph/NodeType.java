package com.probendi.itgraph;

/**
 * A node's type.
 *
 * @author Daniele Di Salvo
 * @since 1.0.0
 */
public enum NodeType {
    /**
     * Indicates that the node is a lexeme.
     */
    LEXEME,
    /**
     * Indicates that the node is a division.
     */
    DIVISION,
    /**
     * Indicates that the node is a binary division.
     */
    OPPOSITION
}
